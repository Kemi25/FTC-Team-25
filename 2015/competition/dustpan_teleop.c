#include "JoystickDriver.c"  //Include file to "handle" the Bluetooth messages.

#define SERVO_ARM_UP    140
#define SERVO_ARM_DOWN  0
#define SERVO_ARM_90CM  130

static int drive_multiplier = 1;

typedef enum brush_state_ {
    BRUSH_OFF,
    BRUSH_FORWARD,
    BRUSH_BACKWARD,
} brush_state_t;

typedef enum arm_state_ {
    ARM_UP,
    ARM_DOWN,
    ARM_90CM,
} arm_state_t;

typedef enum joystick_event_ {
    RIGHT_TRIGGER_UP = 6,
    RIGHT_TRIGGER_DOWN = 8,
    LEFT_TRIGGER_UP = 5,
    LEFT_TRIGGER_DOWN = 7,
    BUTTON_ONE = 1,
    BUTTON_FOUR = 4,
    BUTTON_TWO = 2,
    BUTTON_SIX = 6,
    BUTTON_EIGHT = 8,
} joystick_event_t;

brush_state_t brush_state;
arm_state_t arm_state;

bool debounce;

task debounceTask()
{
    debounce = true;
    wait1Msec(500);
    debounce = false;
}

void all_stop()
{
    motor[driveFrontLeft] = 0;
    motor[driveRearLeft] = 0;
    motor[driveFrontRight] = 0;
    motor[driveRearRight] = 0;
}

void brush_enter_state(brush_state_t state)
{
    brush_state = state;

    switch (state) {
    case BRUSH_OFF:
        servo[brush] = 127;
        break;
    case BRUSH_FORWARD:
        servo[brush] = 5;
        break;
    case BRUSH_BACKWARD:
        servo[brush] = 250;
        break;
    }
}

void arm_enter_state(arm_state_t state)
{
    arm_state = state;

    switch (state) {
    case ARM_UP:
        servo[arm] = SERVO_ARM_UP;
        break;
    case ARM_DOWN:
        servo[arm] = SERVO_ARM_DOWN;
        break;
    case ARM_90CM:
    		servo[arm] = SERVO_ARM_90CM;
    		break;
    }
}

void initializeRobot()
{
    brush_enter_state(BRUSH_OFF);
    arm_enter_state(ARM_DOWN);

    all_stop();

    return;
}

void handle_joy1_btn6()
{
	switch (arm_state) {
	case ARM_UP:
			arm_enter_state(ARM_90CM);
			break;
	case ARM_DOWN:
			arm_enter_state(ARM_90CM);
			break;
	case ARM_90CM:
			arm_enter_state(ARM_90CM);
			break;
  }
}

